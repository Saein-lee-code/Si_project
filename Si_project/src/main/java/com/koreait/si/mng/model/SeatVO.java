package com.koreait.si.mng.model;

public class SeatVO {
	private int i_seat;	
	private String seat_info;
	private int std_no;
	private String s_dt;
	private String o_dt;
	
	public int getI_seat() {
		return i_seat;
	}
	public void setI_seat(int i_seat) {
		this.i_seat = i_seat;
	}
	public String getSeat_info() {
		return seat_info;
	}
	public void setSeat_info(String seat_info) {
		this.seat_info = seat_info;
	}
	public int getStd_no() {
		return std_no;
	}
	public void setStd_no(int std_no) {
		this.std_no = std_no;
	}
	public String getS_dt() {
		return s_dt;
	}
	public void setS_dt(String s_dt) {
		this.s_dt = s_dt;
	}
	public String getO_dt() {
		return o_dt;
	}
	public void setO_dt(String o_dt) {
		this.o_dt = o_dt;
	}
}
